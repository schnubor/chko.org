    {
        $video = Video::create([
            'project_id' => $request->input('project_id'),
            'youtube_id' => $request->input('youtube_id')
        ]);
        if($video){
            flash()->success('Video added successfully.');
        }
        else{
            flash()->error('Oops! Something went wrong.');
        }
        return redirect(route('backend'));
    }

